    suspend fun categorize(signals: PageSignals, webshrinkerCategories: List<SiteCategory> = emptyList()): CategoryResult {
        val domain = signals.domain?.lowercase()?.removePrefix("www.") ?: ""
        val fullUrl = signals.url.lowercase()

        val scores = mutableMapOf<SiteCategory, Float>()
        fun addScore(category: SiteCategory, weight: Float) {
            scores[category] = (scores[category] ?: 0f) + weight
        }

        if (signals.isSafeBrowsingFlagged || signals.isUrlHausFlagged) {
            if (signals.isBrandImpersonation) {
                addScore(SiteCategory.PHISHING, 95f)
            } else {
                addScore(SiteCategory.MALWARE, 95f)
            }
        }
        if (signals.isVirusTotalFlagged) {
            addScore(SiteCategory.MALWARE, 85f)
        }
        if (signals.isBrandImpersonation) {
            addScore(SiteCategory.PHISHING, 90f)
        }
        // ── Extra API signal scoring ──
        if (signals.isUrlScanMalicious) addScore(SiteCategory.MALWARE, 50f)
        if (signals.isCloudflareMalicious) addScore(SiteCategory.MALWARE, 50f)
        if (signals.isSpamhausListed) addScore(SiteCategory.PHISHING, 40f)
        if (signals.isCleanBrowsingBlocked) addScore(SiteCategory.PORNOGRAPHY, 35f)
        if (signals.isSymantecMalicious) addScore(SiteCategory.MALWARE, 60f)
        if (signals.isTalosMalicious) addScore(SiteCategory.MALWARE, 60f)
        if (signals.vtPositives >= 3) addScore(SiteCategory.MALWARE, (signals.vtPositives * 10f).coerceAtMost(60f))
        if (signals.abuseConfidenceScore > 50) addScore(SiteCategory.MALWARE, 30f)
        if (signals.isNsfwLikely) addScore(SiteCategory.PORNOGRAPHY, 40f)
        if (signals.domainAgeDays != null && signals.domainAgeDays < 30) addScore(SiteCategory.PHISHING, 20f)
        if (signals.sslGrade == "F") addScore(SiteCategory.PHISHING, 15f)

        val negativeReasons = signals.communityReportReasons.filter { !it.startsWith("👍") }
        if (negativeReasons.size >= 3) {
            val nl = negativeReasons.joinToString(" ").lowercase()
            if (nl.contains("phish") || nl.contains("fake")) {
                addScore(SiteCategory.PHISHING, 85f)
            }
            if (nl.contains("malware") || nl.contains("virus")) {
                addScore(SiteCategory.MALWARE, 80f)
            }
            if (nl.contains("adult") || nl.contains("porn") || nl.contains("nsfw")) {
                addScore(SiteCategory.PORNOGRAPHY, 80f)
            }
        }

        // 1. Known Domain
        lookupDomain(domain)?.let { addScore(it, 100f) }
        // 2. TLD
        lookupTLD(domain)?.let { addScore(it, 30f) }
        // 2.5 Domain Keywords — reduced weight to 20f to prevent false positives from substring matches
        extractCategoryFromDomainKeywords(domain)?.let { addScore(it, 20f) }
        // 3. OG Type
        lookupOgType(signals.ogType)?.let { addScore(it, 40f) }
        // 4. URL Path
        extractCategoryFromUrlPath(fullUrl)?.let { addScore(it, 25f) }
        // 5. Schema.org
        extractCategoryFromSchema(signals.bodyText)?.let { addScore(it, 60f) }
        
        // ── 5.5 ENTERPRISE: Webshrinker Categories ──
        for (wsCat in webshrinkerCategories) {
            val enterpriseBoost = when (wsCat.threatLevel) {
                ThreatLevel.DANGEROUS -> 1000f
                ThreatLevel.CAUTION -> 500f
                ThreatLevel.SAFE -> 200f
            }
            addScore(wsCat, enterpriseBoost) // Enterprise DB overrides weak heuristics
        }
        // 6. Cloudflare Radar API category weightage (50-point bonus)
        val cfCat = signals.cloudflareCategory?.lowercase() ?: ""
        if (cfCat.isNotBlank()) {
            val cfCategory = when {
                cfCat.contains("malware") -> SiteCategory.MALWARE
                cfCat.contains("phishing") -> SiteCategory.PHISHING
                cfCat.contains("spam") -> SiteCategory.PHISHING
                cfCat.contains("adult") || cfCat.contains("porn") -> SiteCategory.PORNOGRAPHY
                cfCat.contains("gambling") || cfCat.contains("casino") -> SiteCategory.ONLINE_CASINOS
                cfCat.contains("piracy") || cfCat.contains("copyright") -> SiteCategory.SOFTWARE_PIRACY
                cfCat.contains("shopping") || cfCat.contains("ecommerce") || cfCat.contains("e-commerce") -> SiteCategory.ONLINE_RETAIL
                cfCat.contains("news") || cfCat.contains("media") -> SiteCategory.NATIONAL_NEWS
                cfCat.contains("education") || cfCat.contains("academic") -> SiteCategory.SCHOOLS_UNIVERSITIES
                cfCat.contains("technology") || cfCat.contains("software") -> SiteCategory.TECH_NEWS
                cfCat.contains("social") || cfCat.contains("networking") -> SiteCategory.SOCIAL_MEDIA
                cfCat.contains("entertainment") || cfCat.contains("streaming") -> SiteCategory.MOVIE_STREAMING
                cfCat.contains("finance") || cfCat.contains("banking") || cfCat.contains("financial") -> SiteCategory.BANKING
                cfCat.contains("health") || cfCat.contains("medical") || cfCat.contains("pharma") -> SiteCategory.MEDICAL_INFORMATION
                cfCat.contains("government") || cfCat.contains("politics") -> SiteCategory.GOVERNMENT_PORTALS
                cfCat.contains("travel") || cfCat.contains("tourism") || cfCat.contains("hotel") -> SiteCategory.HOTEL_BOOKING
                cfCat.contains("food") || cfCat.contains("restaurant") || cfCat.contains("recipe") -> SiteCategory.FOOD_DELIVERY
                cfCat.contains("sport") -> SiteCategory.SPORTS_NEWS
                cfCat.contains("gaming") || cfCat.contains("game") -> SiteCategory.GAMING
                cfCat.contains("music") || cfCat.contains("audio") -> SiteCategory.MUSIC_STREAMING
                cfCat.contains("auto") || cfCat.contains("vehicle") || cfCat.contains("car") -> SiteCategory.AUTOMOTIVE
                cfCat.contains("real estate") || cfCat.contains("property") -> SiteCategory.REAL_ESTATE_FINANCE
                cfCat.contains("job") || cfCat.contains("career") || cfCat.contains("recruit") -> SiteCategory.PROFESSIONAL_NETWORKING
                cfCat.contains("legal") || cfCat.contains("law") -> SiteCategory.LEGAL_SERVICES
                cfCat.contains("religion") || cfCat.contains("spiritual") -> SiteCategory.RELIGIOUS_INSTITUTIONS
                cfCat.contains("crypto") || cfCat.contains("blockchain") -> SiteCategory.CRYPTOCURRENCY
                cfCat.contains("forum") || cfCat.contains("community") -> SiteCategory.FORUMS_COMMUNITIES
                cfCat.contains("blog") -> SiteCategory.BLOGGING
                cfCat.contains("dating") -> SiteCategory.DATING_LEGIT
                cfCat.contains("weather") -> SiteCategory.WEATHER
                else -> null
            }
            if (cfCategory != null) {
                addScore(cfCategory, 85f) // Enterprise API signal — highly authoritative
            }
        }

        // 6b. OG Site Name bonus — if site name matches a known brand
        val siteName = signals.ogSiteName.lowercase()
        if (siteName.isNotBlank()) {
            lookupDomain("$siteName.com")?.let { addScore(it, 40f) }
        }

        // ── 6c. Title Phrase Matching — require 2+ phrase matches and use specific phrases ──
        // Removed overly generic terms: "sign up", "join now", "create account", "menu", "learn ", "apply now"
        val titleLower = signals.scrapedTitle.lowercase()
        if (titleLower.length >= 5) {
            data class TitleMatch(val category: SiteCategory, val phrases: List<String>)
            val titleMatches = listOf(
                TitleMatch(SiteCategory.ONLINE_RETAIL, listOf("buy online", "shop now", "best deals", "free shipping", "add to cart", "best price", "shopping")),
                TitleMatch(SiteCategory.NATIONAL_NEWS, listOf("breaking news", "latest news", "live updates", "headlines", "top stories")),
                TitleMatch(SiteCategory.ONLINE_LEARNING, listOf("online course", "tutorial", "certification", "syllabus", "enroll now", "e-learning")),
                TitleMatch(SiteCategory.MOVIE_STREAMING, listOf("watch online", "stream now", "full movie", "watch free", "streaming")),
                TitleMatch(SiteCategory.PROFESSIONAL_NETWORKING, listOf("job opening", "careers", "hiring", "work from home", "vacancies", "recruitment")),
                TitleMatch(SiteCategory.BANKING, listOf("savings account", "fixed deposit", "loan apply", "credit card apply", "net banking", "internet banking")),
                TitleMatch(SiteCategory.FLIGHT_BOOKING, listOf("book flight", "cheap flights", "hotel deals", "vacation package", "airline tickets")),
                TitleMatch(SiteCategory.FOOD_DELIVERY, listOf("order food", "food delivery", "restaurant near", "food online", "takeaway")),
                TitleMatch(SiteCategory.ONLINE_CASINOS, listOf("bet now", "casino", "place bet", "free spins", "jackpot", "sportsbook")),
                TitleMatch(SiteCategory.CRYPTOCURRENCY, listOf("buy bitcoin", "buy crypto", "trade crypto", "defi", "blockchain")),
                TitleMatch(SiteCategory.PORNOGRAPHY, listOf("porn", "xxx", "adult video", "nsfw")),
                TitleMatch(SiteCategory.SOFTWARE_PIRACY, listOf("download free", "torrent", "watch free movie", "cracked")),
                TitleMatch(SiteCategory.MEDICAL_INFORMATION, listOf("symptoms of", "treatment for", "diagnosis", "doctor appointment", "health tips")),
            )
            for (tm in titleMatches) {
                val hits = tm.phrases.count { titleLower.contains(it) }
                // For DANGEROUS categories, 1 match is enough. For SAFE, require 2+.
                val minHits = if (tm.category.threatLevel == ThreatLevel.DANGEROUS || tm.category.threatLevel == ThreatLevel.CAUTION) 1 else 2
                if (hits >= minHits) {
                    addScore(tm.category, 30f + (hits - 1) * 10f) // 30 base + 10 per extra match
                }
            }
        }

        // ── 6d. Meta Keywords Cross-Reference ──
        val metaKw = signals.metaKeywords.lowercase()
        if (metaKw.length >= 5) {
            for (signal in KEYWORD_SIGNALS) {
                val metaMatches = signal.keywords.count { kw -> metaKw.contains(kw) }
                if (metaMatches >= 2) {
                    addScore(signal.category, 20f)
                }
            }
        }

        // 7. Keywords
        applyKeywordWeights(signals, ::addScore)

        // 7b. Description-based keyword matching — meta description is an authoritative signal
        val descLower = signals.scrapedDescription.lowercase()
        if (descLower.length >= 15) {
            for (signal in KEYWORD_SIGNALS) {
                val descMatches = signal.keywords.count { kw -> descLower.contains(kw) }
                if (descMatches >= 1) {
                    // Description matches are high-value (page author describes the site)
                    addScore(signal.category, descMatches * 20f)
                }
            }
        }

        // 8. Try AI if nothing matched well enough
        val bestScoreSoFar = scores.values.maxOrNull() ?: 0f
        if (scores.isEmpty() || bestScoreSoFar < 25f) {
             val aiResult = tryAIClassification(signals)
             if (aiResult != null) return aiResult
        }

        // Evaluate Best Match - prioritizing DANGEROUS/CAUTION in the event of ties or near-ties
        val sortedMatches = scores.entries.sortedWith(
            compareByDescending<Map.Entry<SiteCategory, Float>> { it.value }
                .thenByDescending { it.key.threatLevel.ordinal }
        )
        
        val bestMatch = sortedMatches.firstOrNull()
        val secondBest = sortedMatches.getOrNull(1)
        
        if (bestMatch != null) {
            val absolutelyConfident = bestMatch.value >= 30f
            // Relative confidence: if best is 2x the runner-up, accept even at lower absolute scores
            val relativelyConfident = bestMatch.value >= 20f && 
                (secondBest == null || bestMatch.value >= secondBest.value * 2f)
            
            if (absolutelyConfident || relativelyConfident) {
                return CategoryResult(bestMatch.key, minOf(bestMatch.value / 100f, 0.95f), "Heuristic Match (Score: ${bestMatch.value})")
            }
        }
        
        // 9. Last resort: try AI even if we had some weak signals
        if (bestScoreSoFar > 0f) {
            val aiResult = tryAIClassification(signals)
            if (aiResult != null) return aiResult
        }

        return CategoryResult(SiteCategory.UNKNOWN, 0.5f, "No strong heuristic signals found.")
    }

    private fun lookupDomain(domain: String): SiteCategory? {
        // 1. Try exact match first (cloud then local)
        val cloudMap = com.safeqr.scanner.data.remote.CloudDatasetManager.getWebsiteCategorizerData().KNOWN_DOMAINS
        val catName = cloudMap[domain]
        if (catName != null) {
            return SiteCategory.entries.find { it.name == catName }
        }
        WebsiteCategorizerDefaults.KNOWN_DOMAINS[domain]?.let { return it }

        // 2. Subdomain-aware fallback: strip subdomains progressively
        //    e.g., "news.google.com" → "google.com" → match!
        val parts = domain.split(".")
        if (parts.size > 2) {
            for (i in 1 until parts.size - 1) {
                val parent = parts.subList(i, parts.size).joinToString(".")
                val cloudParent = cloudMap[parent]
                if (cloudParent != null) {
                    return SiteCategory.entries.find { it.name == cloudParent }
                }
                WebsiteCategorizerDefaults.KNOWN_DOMAINS[parent]?.let { return it }
            }
        }
        return null
    }

    private fun lookupTLD(domain: String): SiteCategory? {
        if (domain.isEmpty()) return null
        val cloudMap = com.safeqr.scanner.data.remote.CloudDatasetManager.getWebsiteCategorizerData().TLD_CATEGORY_MAP
        if (cloudMap.isNotEmpty()) {
            val match = cloudMap.entries
                .sortedByDescending { it.key.length }
                .firstOrNull { (tld, _) -> domain.endsWith(tld) }
            if (match != null) {
                return SiteCategory.entries.find { it.name == match.value }
            }
        }
        
        return WebsiteCategorizerDefaults.TLD_CATEGORY_MAP.entries
            .sortedByDescending { it.key.length }
            .firstOrNull { (tld, _) -> domain.endsWith(tld) }
            ?.value
    }

    private fun lookupOgType(ogType: String): SiteCategory? {
        if (ogType.isEmpty()) return null
        val cloudMap = com.safeqr.scanner.data.remote.CloudDatasetManager.getWebsiteCategorizerData().OG_TYPE_MAP
        val catName = cloudMap[ogType]
        if (catName != null) {
