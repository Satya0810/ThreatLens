    }

    private fun extractCategoryFromDomainKeywords(domain: String): SiteCategory? {
        if (domain.isEmpty() || domain.length < 5) return null
        // Strip the TLD to avoid matching things like .shop as a domain keyword if it's actually a TLD
        val domainBody = domain.substringBeforeLast(".")
        
        // Use word-boundary-aware matching to avoid substring false positives
        // e.g., "betterhelp" should NOT match "bet", "postman" should NOT match "post"
        fun domainContainsWord(word: String): Boolean {
            // A "word" in a domain is delimited by hyphens, dots, or string boundaries
            // Also match if the domain body exactly equals the word
            return Regex("(^|[.-])${Regex.escape(word)}([.-]|$)").containsMatchIn(domainBody)
        }
        
        return when {
            domainContainsWord("bank") || domainContainsWord("banking") -> SiteCategory.BANKING
            domainContainsWord("shop") || domainContainsWord("store") || domainContainsWord("retail") || domainContainsWord("mart") -> SiteCategory.ONLINE_RETAIL
            domainContainsWord("news") || domainContainsWord("daily") || domainContainsWord("times") -> SiteCategory.NATIONAL_NEWS
            domainContainsWord("tech") -> SiteCategory.TECH_NEWS
            domainContainsWord("movie") || domainContainsWord("cinema") || domainContainsWord("stream") -> SiteCategory.MOVIE_STREAMING
            domainContainsWord("game") || domainContainsWord("gaming") -> SiteCategory.GAMING
            domainContainsWord("school") || domainBody.contains("university") || domainContainsWord("college") || domainContainsWord("academy") -> SiteCategory.SCHOOLS_UNIVERSITIES
            domainContainsWord("clinic") || domainContainsWord("hospital") || domainContainsWord("health") -> SiteCategory.MEDICAL_INFORMATION
            domainContainsWord("travel") || domainContainsWord("hotel") || domainContainsWord("flight") -> SiteCategory.FLIGHT_BOOKING
            domainContainsWord("food") || domainContainsWord("restaurant") || domainContainsWord("pizza") || domainContainsWord("burger") -> SiteCategory.FOOD_DELIVERY
            domainContainsWord("crypto") || domainContainsWord("coin") || domainContainsWord("token") -> SiteCategory.CRYPTOCURRENCY
            domainContainsWord("bet") || domainContainsWord("casino") -> SiteCategory.ONLINE_CASINOS
            domainContainsWord("porn") || domainContainsWord("xxx") || domainContainsWord("sex") -> SiteCategory.PORNOGRAPHY
            else -> null
        }
    }

