/**
 * Deterministic Multi-Signal Website Categorization Engine.
 *
 * Uses FIVE independent signals (in priority order) to reliably classify websites:
 *   1. Known Domain Database   — instant, 100% accurate for ~300+ popular domains
 *   2. TLD / Domain Structure  — .edu → Education, .gov → Government, etc.
 *   3. Open Graph Metadata     — og:type, og:site_name from the scraped page
 *   4. Keyword Density Analysis — uses keyword counts from Jsoup body text scrape
 *   5. AI Fallback (Optional)  — HuggingFace zero-shot as a last resort
 *
 * Every website ALWAYS gets a rich, descriptive category — never just "Safe".
 * The category's inherent threat level directly drives the final SafetyStatus.
 */
